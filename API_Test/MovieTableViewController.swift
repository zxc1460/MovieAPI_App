    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: "MovieTableViewCell") as? MovieTableViewCell else {
            return UITableViewCell()
        }
        
        cell.titleLabel.text = data[indexPath.row].title.tagRemoved
        cell.ratingLabel.text = data[indexPath.row].userRating
        cell.directorLabel.text = data[indexPath.row].director
        cell.actorLabel.text = data[indexPath.row].actor
        
        DispatchQueue(label: "background").async {
            do {
                if let url = URL(string: self.data[indexPath.row].image) {
                    let imageData = try Data(contentsOf: url)
                    if let img = UIImage(data: imageData) {
                        DispatchQueue.main.async {
                            cell.thumbnailImageView.image = img
                        }
                    }
                }
            } catch {
                print(error.localizedDescription)
            }
        }
        
        return cell
    }
    

